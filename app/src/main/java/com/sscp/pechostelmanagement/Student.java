package com.sscp.pechostelmanagement;

public class Student {
    String userid, name, email, pass, mobile, imageurl, branch, year, sem;

    Student(){}

    public Student(String userid, String name, String email, String pass, String mobile, String imageurl, String branch, String year, String sem) {
        this.userid = userid;
        this.name = name;
        this.email = email;
        this.pass = pass;
        this.mobile = mobile;
        this.imageurl = imageurl;
        this.branch = branch;
        this.year = year;
        this.sem = sem;
    }

    public String getBranch() {
        return branch;
    }

    public void setBranch(String branch) {
        this.branch = branch;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getSem() {
        return sem;
    }

    public void setSem(String sem) {
        this.sem = sem;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getImageurl() {
        return imageurl;
    }

    public void setImageurl(String imageurl) {
        this.imageurl = imageurl;
    }
}
